package com.cadre.wvds.sniffer;

public class TraceFile {

    private int fid;
    private String path;

    public TraceFile() { }

    public int getFid() {
        return fid;
    }

    public void setFid(int fid) {
        this.fid = fid;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

}
